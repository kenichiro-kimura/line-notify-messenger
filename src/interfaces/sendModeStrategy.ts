/**
 * メッセージ送信モードを表す列挙型
 * LINE通知の送信先タイプを定義します
 */
export enum SendMode {
    /** ブロードキャスト送信（全ての友達向け一斉配信） */
    broadcast = 'broadcast',
    /** グループ送信（所属しているグループ向け配信） */
    group = 'group',
    /** すべての送信先（ブロードキャストとグループの両方） */
    all = 'all'
}

/**
 * メッセージ送信モード決定のための戦略インターフェース
 * 異なる送信モード決定ロジックを実装するためのコントラクトを定義します
 */
export interface ISendModeStrategy {
    /**
     * 現在の送信モードを取得します
     * 実装クラスは環境変数やシステム設定などから適切な送信モードを決定します
     * @returns 使用すべき送信モード
     */
    getSendMode(): SendMode;
}
